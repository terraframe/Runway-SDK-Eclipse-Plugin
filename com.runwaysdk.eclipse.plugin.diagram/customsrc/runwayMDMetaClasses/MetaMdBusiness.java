package runwayMDMetaClasses;

public class MetaMdBusiness extends MetaMdElement {
	
	private String superMdClass;
	private String cacheAlgorithm;
	private String cacheSize;
	
	
	public String getSuperMdClass() {
		return superMdClass;
	}
	public void setSuperMdClass(String superMdClass) {
		this.superMdClass = superMdClass;
	}
	public String getCacheAlgorithm() {
		return cacheAlgorithm;
	}
	public void setCacheAlgorithm(String cacheAlgorithm) {
		this.cacheAlgorithm = cacheAlgorithm;
	}
	public String getCacheSize() {
		return cacheSize;
	}
	public void setCacheSize(String cacheSize) {
		this.cacheSize = cacheSize;
	}
	
	
	

}
